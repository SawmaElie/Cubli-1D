/**
  * @brief This function handles TIM2 global interrupt.
  */
void TIM2_IRQHandler(void)
{
  /* USER CODE BEGIN TIM2_IRQn 0 */

  /* USER CODE END TIM2_IRQn 0 */
  HAL_TIM_IRQHandler(&htim2);
  /* USER CODE BEGIN TIM2_IRQn 1 */
countpulse();  // Calculate encoder pulses

  // Read MPU6050
  MPU6050_Read_Accel(&hi2c1, &MPU6050);
  MPU6050_Read_Gyro(&hi2c1, &MPU6050);

  // Calculate tilt angle using filters
  angle_calculate(
    MPU6050.Accel_X_RAW,
    MPU6050.Accel_Y_RAW,
    MPU6050.Accel_Z_RAW,
    MPU6050.Gyro_X_RAW,
    MPU6050.Gyro_Y_RAW,
    MPU6050.Gyro_Z_RAW,
    dt, Q_angle, Q_gyro, R_angle, C_0, K1
  );

  PD();  // Angle PD control
  ReactionWheelPWM();  // Motor PWM logic

  // Run Speed PI control every 40ms
  cc++;
  if (cc >= 8) {
    SpeedPIout();
    cc = 0;
  }
  /* USER CODE END TIM2_IRQn 1 */
}

